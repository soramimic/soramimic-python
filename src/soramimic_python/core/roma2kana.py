def _zenkaku_english_to_hankaku(text: str) -> str:
    """全角英字を半角へ（re.sub は全体に適用されます）"""
    def conv(m):
        ch = m.group(0)
        return chr(ord(ch) - 65248)
    return re.sub(r"[Ａ-Ｚａ-ｚ]", conv, text)


def convert_roman_to_kana(original: str, tree: Dict[str, Any] = DEFAULT_TREE) -> str:
    """
    JS convertRomanToKana を Python に移植。
    - 全角英字→半角→小文字化
    - トライ木でマッチ（葉が文字列なら確定出力）
    - 重子音: 直前と同じ子音で 'ッ'
    - 'n' の単独や末尾: 'ン'
    - マッチしない場合は元文字をそのまま出力
    """
    # 全角→半角→小文字（JS相当）
    s = _zenkaku_english_to_hankaku(original).lower()
    result = ""
    tmp = ""       # 未確定の原文断片
    index = 0
    node = tree

    def push(chars: str, to_root: bool = True):
        nonlocal result, tmp, node
        result += chars
        tmp = ""
        node = tree if to_root else node

    while index < len(s):
        ch = s[index]
        if re.match(r"[a-z]", ch):
            if ch in node:
                nxt = node[ch]
                if isinstance(nxt, str):
                    # ここで確定して出力、ルートへ戻る
                    push(nxt)
                else:
                    # 中間ノード：原文の文字を保留しつつ、ノードを降りる
                    tmp += original[index]
                    node = nxt
                index += 1
                continue

            # 一致しなかったときの処理（促音 / n）
            prev = s[index - 1] if index > 0 else ""
            if prev and (prev == "n" or prev == ch):
                push("ン" if prev == "n" else "ッ", to_root=False)

            # 途中のノードから仕切り直し（ルートに戻して再判定）
            if node is not tree and ch in tree:
                push(tmp)  # 保留分をそのまま出力
                continue

        # 英字以外、またはマッチ失敗時は保留＋現文字を出力
        push(tmp + original[index])
        index += 1

    # 末尾の n は ン に
    if tmp.endswith("n"):
        tmp = re.sub(r"n$", "ン", tmp)
    push(tmp)

    return result